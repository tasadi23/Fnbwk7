#advanced concepts - strings

message = ' Hello, World! '

print(len(message))
print(message.strip()) # Remove leading and trailing whitespace
print(message.lower()) # Convert all characters to lower case
print(message.split(',')) # Split the string into a list based on the comma

# Try the following on your own:

print(message.upper()) # Convert all character to upper case
print(message.replace(',', ' ')) # Replace the string into a list based on the comma